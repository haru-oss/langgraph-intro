# ・短期記憶
# ・長期記憶
# ・State分離
# ・Graph分岐
# ・Tool呼び出し
#
# などが入る。
#
# しかし根本思想は変わらない。
#
# ずっと：
#
# 「State更新」
#
# をしている。

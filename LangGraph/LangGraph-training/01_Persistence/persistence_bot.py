# ----------------------------------------------------------------------------------------------------------
# 【ここまで理解したLangGraph / Python / State の超重要まとめ】
#
# ■ LangGraphの本質
#
# LangGraph は本質的には、
#
# 「State（状態）」を流し続けるシステム。
#
# 流れとしては：
#
# State
# ↓
# Node実行
# ↓
# State更新
# ↓
# 次Node
# ↓
# State更新
# ↓
# Memory保存
#
# これを繰り返している。
#
# つまり LangGraph は、
#
# 「状態遷移グラフエンジン」
#
# に近い。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ Pythonのクラス・インスタンス理解
#
# class Dog:
#     pass
#
# ↑ これは Dog という「設計図（クラス）」を作っている。
#
# pochi = Dog()
#
# ↑ これは Dogクラスから pochi という「実体（インスタンス）」を生成している。
#
# 用語整理：
#
# クラス = 設計図
# インスタンス = 設計図から生成された実体
# オブジェクト = 実体そのもの（インスタンスとほぼ同じ意味）
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ __init__ とは？
#
# class Dog:
#     def __init__(self, name):
#         self.name = name
#
# pochi = Dog("Pochi")
#
# ↑ Dog("Pochi") が実行される時、内部では __init__ が呼ばれる。
#
# __init__ は「インスタンス生成時の初期設定」。
#
# self は「生成された自分自身のインスタンス」。
#
# self.name = name
# は、
#
# 「このインスタンスの中に name を保存する」
#
# という意味。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ class State(TypedDict): の意味
#
# class State(TypedDict):
#     messages: Annotated[list, add_messages]
#
# ここで最初かなり混乱した。
#
# なぜなら：
#
# 「継承 = 機能を引き継ぐ」
#
# だと思っていたから。
#
# しかし TypedDict は特殊。
#
# TypedDict は：
#
# 「型付きdictを作るための特殊クラス」
#
# だった。
#
# つまりこれは：
#
# 「Stateというdict構造を定義している」
#
# だけ。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ TypedDict はGraph機能を持っていない
#
# 最初：
#
# 「TypedDict に add_node や compile がある？」
#
# と勘違いした。
#
# しかし違う。
#
# TypedDict は：
#
# ・dict構造
# ・キー
# ・型
#
# を定義するだけ。
#
# Graph機能は持たない。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ messages: Annotated[list, add_messages] の意味
#
# ここも超重要。
#
# Annotated は：
#
# Annotated[型, メタ情報]
#
# という構文。
#
# つまり：
#
# messages: Annotated[list, add_messages]
#
# は、
#
# 「messages は本体としては list 型」
#
# だけど、
#
# 「LangGraph向けに add_messages という追加情報を付与」
#
# している。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ add_messages の意味
#
# 普通のlist更新：
#
# old = ["A"]
# new = ["B"]
#
# old = new
#
# ↑ これは上書き。
#
#
# しかし add_messages の場合：
#
# old + new
#
# のように、
#
# 「後ろに追加」
#
# を行う。
#
# つまり：
#
# messages は、
#
# 「会話履歴list」
#
# として扱われる。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ messages の実態
#
# 実際の messages はこんな感じ：
#
# [
#     HumanMessage(content="私の名前は田中です。"),
#
#     AIMessage(
#         content="こんにちは田中さん！"
#     ),
#
#     HumanMessage(
#         content="私の名前を覚えていますか？"
#     ),
#
#     AIMessage(
#         content="はい、田中さんですね。"
#     )
# ]
#
# つまり：
#
# 「ユーザーとAIの会話履歴を順番に保持したlist」
#
# になっている。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ HumanMessage / AIMessage とは？
#
# HumanMessage は：
#
# 「人間側メッセージ」
#
# を表すLangChainのオブジェクト。
#
# AIMessage は：
#
# 「AI側メッセージ」
#
# を表すオブジェクト。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ builder = StateGraph(State) の意味
#
# ここが最大の混乱ポイントだった。
#
# 最初：
#
# 「StateGraph が State のインスタンス？」
#
# と混乱した。
#
# しかし正しくは：
#
# builder は、
#
# 「StateGraphクラスのインスタンス」
#
# である。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ Dog例で整理
#
# class Dog:
#     pass
#
# pochi = Dog()
#
# ↑ この対応で考えると：
#
# StateGraph = Dog
# builder = pochi
#
# になる。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ では State は何？
#
# ここが超重要。
#
# builder = StateGraph(State)
#
# の State は、
#
# 「Stateクラスそのもの」
#
# を渡している。
#
# つまり：
#
# 「State構造を扱うGraphを作ってください」
#
# という意味。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ Pythonではクラスを引数として渡せる
#
# Pythonでは：
#
# SomeClass(AnotherClass)
#
# のように、
#
# 「クラスそのものを引数として渡す」
#
# 文化がある。
#
# LangGraphもそれ。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ StateGraph(State) のイメージ
#
# 感覚的には：
#
# builder = StateGraph(
#     使用する状態構造 = State
# )
#
# のような感じ。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ Nodeとは？
#
# def chatbot(state: State):
#
# これは：
#
# 「Stateを受け取り、
# Stateを更新して返す関数」
#
# に近い。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ chatbot の流れ
#
# state["messages"]
#
# で現在会話履歴を取得。
#
# それを：
#
# llm.invoke(state["messages"])
#
# に渡す。
#
# するとAIが返答する。
#
# その結果を：
#
# {
#     "messages": [AIMessage(...)]
# }
#
# として返す。
#
# LangGraph側が：
#
# add_messages
#
# を見て、
#
# 「messagesをappend的にマージ」
#
# する。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ builder.add_node() の意味
#
# builder.add_node("chatbot", chatbot)
#
# は：
#
# 「chatbotノードに chatbot関数を登録」
#
# している。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ add_edge() の意味
#
# builder.add_edge(START, "chatbot")
#
# は：
#
# START → chatbot
#
# という線を引いている。
#
# つまり：
#
# 「どのNodeからどのNodeへ流れるか」
#
# を定義している。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ compile() の意味
#
# graph = builder.compile(checkpointer=memory)
#
# は：
#
# 「設計図(builder)を
# 実行可能Graphへ変換」
#
# している。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ checkpointer とは？
#
# memory = MemorySaver()
#
# は：
#
# 「State保存装置」
#
# である。
#
# 今回はオンメモリ保存。
#
# 本番では：
#
# ・SQLite
# ・Postgres
# ・Redis
#
# などを使う。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ thread_id の意味
#
# config = {
#     "configurable": {
#         "thread_id": "user-1"
#     }
# }
#
# これは：
#
# 「誰の会話履歴か」
#
# を識別している。
#
# thread_id が同じなら、
# 会話履歴(State)が引き継がれる。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ graph.stream() の意味
#
# graph.stream(input_message, config)
#
# は：
#
# 「State更新イベントを順番に流す」
#
# 関数。
#
# event の中に、
#
# 最新State
#
# が入っている。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ value["messages"][-1] の意味
#
# list[-1]
#
# は：
#
# 「最後の要素」
#
# を意味する。
#
# つまり：
#
# value["messages"][-1]
#
# は：
#
# 「最新メッセージ」
#
# を取得している。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ print(state) が超重要
#
# LangGraph理解は：
#
# 「State変化を観察」
#
# するのが最強。
#
# なので：
#
# print(state)
# print(messages)
# print(event)
#
# を大量に見るのが重要。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ 最終理解
#
# LangGraphとは：
#
# 「State（状態）を中心に、
# NodeでStateを更新し、
# Graph構造で流し、
# Memoryへ保存する
# AI状態遷移システム」
#
# である。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ さらに本質
#
# AIエージェント開発の本質は：
#
# 「LLMを呼ぶこと」
#
# より、
#
# 「Stateをどう設計し、
# どう流し、
# どう保存し、
# どう分岐させるか」
#
# にある。
#
#
# ----------------------------------------------------------------------------------------------------------
# ■ ChatGPT級になると？
#

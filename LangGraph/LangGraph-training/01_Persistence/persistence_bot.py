# 環境変数の読み込み
load_dotenv()

# 1. Stateの定義
class State(TypedDict):
    messages: Annotated[list, add_messages]

# 2. ノードの定義
llm = ChatOpenAI(model="gpt-4o-mini")

def chatbot(state: State):
    return {"messages": [llm.invoke(state["messages"])]}

# 3. グラフの構築
# -----------------------これは、設計図に線を書いている感覚だろうか。-----------------------------------------------------------------------------------
builder = StateGraph(State)
# ----------------------------------------------------------------------------------------------------------


builder.add_node("chatbot", chatbot)
# 第一引数に関数名、第二引数にノード名を定義
builder.add_edge(START, "chatbot")
# add_edgeでノードを繋げる。第一が繋ぎめの先端、第二がつなぎの末端
builder.add_edge("chatbot", END)
# 第一が上のつなぎの末端。つまり、start,chatbot,endとなる。たとえば、ここに何かを追加したいとなったらば、chatbot,chatbot2。次に終了したいならchatbot2,ENDとなる。はず。


# 4. チェックポインター（メモリ）の準備
# これが「記憶」を司るデータベースの役割を果たします（今回はオンメモリ）
memory = MemorySaver()
# ----------------------------------------------------------------------------------------------------------
# 学ぶべきこと: Checkpointer (MemorySaver, AsyncSqliteSaver, PostgresSaver など) なぜ必要か: 今のコードだとプログラムが終了すると会話履歴が消えます。Web アプリでは、データベースに State を保存し、thread_id を使って「Aさんの会話の続き」を復元する仕組みが必須です。
